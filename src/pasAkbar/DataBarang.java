package pasAkbar;

public class DataBarang {
    private String nama;
    private Integer kodebarang;
    private String jenisbarang;
    private Integer jumlahbarang;
    private Integer hargabarang;

    public String getNama() {
        return nama;
    }

    public void setNama(String namaBarang) {
        this.nama = nama;
    }

    public String getJenisbarang() {
        return jenisbarang;
    }

    public void setJenisbarang(String jenisbarang) {
        this.jenisbarang = jenisbarang;
    }

    public Integer getKodebarang() {
        return kodebarang;
    }

    public void setKodebarang(Integer kodebarang) {
        this.kodebarang = kodebarang;
    }

    public Integer getJumlahbarang() {
        return jumlahbarang;
    }

    public void setJumlahbarang(Integer jumlahbarang) {
        this.jumlahbarang = jumlahbarang;
    }

    public Integer getHargabarang() {
        return hargabarang;
    }

    public void setHargabarang(Integer hargabarang) {
        this.hargabarang = hargabarang;
    }
}
